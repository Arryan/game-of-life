var board = {
  canvas : document.createElement("canvas"),
  start : function() {
    this.canvas.width = 600;
    this.canvas.height = 600;
    this.canvas.addEventListener("click", this.handleClick);
    this.context = this.canvas.getContext("2d");
    document.body.insertBefore(this.canvas, document.body.childNodes[0]);
    renderBoard();
  },
  handleClick : function(e) {
    let xPos = Math.floor(e.offsetX / 15),
    yPos = Math.floor(e.offsetY / 15);
    grid[yPos][xPos] = grid[yPos][xPos] === 1 ? 0 : 1;
    board.context.fillStyle = grid[yPos][xPos] === 1 ? '#ff0744' : '#ffebee';
    board.context.fillRect(xPos * 15, yPos * 15, 15,15);
  }
};

function startGame() {
  board.start();
  document.getElementById("step-btn")
  .addEventListener("click", function() {
    updateGrid();
    renderBoard();
  });
  document.getElementById("play-btn")
  .addEventListener("click", play);
}

function play() {
  updateGrid();
  renderBoard();
  setTimeout(play, 200);
}

function renderBoard() {
  let w = board.canvas.width / grid[0].length;
  let h = board.canvas.height / grid.length;
  for(let i = 0; i < grid.length; i++) {
    for(let j = 0; j < grid[0].length; j++) {
      if(grid[i][j] === 1) {
        board.context.fillStyle = '#ff0744';
      }
      else {
        board.context.fillStyle = '#ffebee';
      }
      board.context.fillRect(j * w, i * h, w, h);
    }
  }
}
